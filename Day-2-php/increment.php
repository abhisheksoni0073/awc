<?php
/*
PHP Increment / Decrement Operators
++$x	pre-increment		
$x++	Post-increment	
--$x	Pre-decrement		
$x--	Post-decrement	 */

$num = 10;
echo  "<h1>".++$num . "</h1>"; // 1 + 10 = 11
echo  "<h1>".$num++. "</h1>"; // 1 + 10 = 11
echo  "<h1>".--$num . "</h1>"; // 1 + 10 = 11
echo  "<h1>".$num-- . "</h1>"; // 1 + 10 = 11



  ?>